
'''
   Description: Converting celsius to fahrenheit
   Output: See Below
   Known bugs: none
'''

TempInCelsius = eval(input("Enter a degree in celsius"))
TempInFahrenheit = (9/5) * TempInCelsius + 32
print(str(TempInCelsius) + " Celsius is " + str(TempInFahrenheit) + "Fahrenheit")

